function openNav() {
    document.getElementById("mySidebar").style.width = "250px";
    document.getElementById("_openbtn").style.display= "none";
    document.getElementById("op_slide_pic").style.marginLeft= "270px";
}

function closeNav() {
    document.getElementById("mySidebar").style.width = "0";
    document.getElementById("_openbtn").style.display="block";
    document.getElementById("op_slide_pic").style.marginLeft= "50px";
}